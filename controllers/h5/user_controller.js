/**
 * Ê∑ªÂä†Ë¥≠Áâ©ËΩ¶
 * @param {String} userId
 * @param {String} goodsId
 * @return {String} collectionNums
 */
const addToCollection = async (ctx,next)=> {
    var {userId, goodsId} = ctx.request.body
    if(!goodsId || !userId) {
        ctx.status = 401
        ctx.body = {
            data: {
                code: 401,
                msg: 'Áº∫Â∞ëÂøÖÂ°´È°π'
            }
        }
        return
    }

    try {
        /**
         * 1. Ëé∑ÂèñÊâÄÊúâÁöÑÊî∂Ëóè
         * 2. Âà§Êñ≠ÊúâÊ≤°ÊúâÊî∂ËóèËøáËØ•ÂïÜÂìÅ 
         *      Êúâ => num+1
         *      Ê≤°Êúâ => Êî∂Ëóè num:1
         * 3. ËøîÂõû
         */

        await user_Col.findOne({userId:userId},{collectionGoods:1}).then(async res=> {
            
            ctx.status = 200

            var myColGoods = res.collectionGoods  // ÊàëÊâÄÊúâÊî∂ËóèÁöÑÂïÜÂìÅ

            if(myColGoods.length>0) {
                // Âà§Êñ≠ÊúâÊ≤°ÊúâÊî∂ËóèËøá
                let collectionGoodsId = []
                var goodsNums = 0 // Â¶ÇÊûúÊî∂Ëóè‰∫ÜËØ•ÂïÜÂìÅ ÈªòËÆ§ËØ•ÂïÜÂìÅÊúâ‰∏Ä‰∏™
                myColGoods.map(x=> {
                    collectionGoodsId.push(x.goodsId)
                    if(x.goodsId == goodsId) {
                        goodsNums = x.num+1
                    }
                })
                if(collectionGoodsId.indexOf(goodsId)>=0) { // Êî∂ËóèËøá
                    await user_Col.updateOne({
                        'collectionGoods.goodsId':goodsId,
                        userId:userId
                    },{
                        '$set':{
                            'collectionGoods.$.num':goodsNums
                        }
                    })
                    ctx.body = {
                        data: {
                            code: 200,
                            list: await user_Col.findOne({
                                userId:userId
                            },{
                                collectionGoods:1,
                                _id:0
                            })
                        }
                    }
                }else { // Ê≤°Êî∂ËóèËøá
                    let data = {
                        goodsId: goodsId,
                        num: 1
                    }
                    await user_Col.updateOne({userId:userId},{
                        $push:{
                            collectionGoods:data
                        }
                    })
                    ctx.body = {
                        data: {
                            code: 200,
                            list: await user_Col.findOne({
                                userId:userId
                            },{
                                collectionGoods:1,
                                _id:0
                            })
                        }
                    }
                }
            }else { // Ê≤°ÊúâÊî∂ËóèËøá‰ªª‰ΩïÂïÜÂìÅ
                let data = {
                    goodsId: goodsId,
                    num: 1
                }
                await user_Col.updateOne({userId:userId},{
                    $push:{
                        collectionGoods:data
                    }
                })
                ctx.body = {
                    data: {
                        code: 200,
                        list: await user_Col.findOne({
                            userId:userId
                        },{
                            collectionGoods:1,
                            _id:0
                        })
                    }
                }
            }
        })


    } catch (error) {
        ctx.status = 500
        ctx.body = {
            data: {
                code: 500,
                msg: error
            }
        }
    }

}

